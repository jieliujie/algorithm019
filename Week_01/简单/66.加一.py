# @lc code=start
class Solution:
    def plusOne(self, digits: List[int]) -> List[int]:
        digits_size = len(digits)
        for i in range(digits_size - 1, -1, -1):
            if digits[i] < 9:
                digits[i] += 1
                return digits
            digits[i] = 0
        return [1] + digits
            
# @lc code=end

